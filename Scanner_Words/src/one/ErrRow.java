package one;

public class ErrRow {

	private int errRow; 
	private String errStr;
	
	public ErrRow(){
		this.errRow = 0;
		this.errStr = "";
	}

	public int getErrRow() {
		return errRow;
	}

	public void setErrRow(int errRow) {
		this.errRow = errRow;
	}

	public String getErrStr() {
		return errStr;
	}

	public void setErrStr(String errStr) {
		this.errStr = errStr;
	}
	
	

}
